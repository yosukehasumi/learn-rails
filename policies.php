<h2>Policies</h2>

<p>Come to class! Attendence is important as I'll be giving quizzes to ensure that course material is being learned and gauge the class progress. Be ready and engaged, you’re logged in, and ready for action. </p>

<h2>Weekly Assignments</h2>

<p>Each week you’ll have a take home assignment. Weekly assignments are always due by 9pm the day before the next class (Monday 9pm). I’ll post specs and grading criteria on the class website and you’ll have a chance to ask any questions at the end of class.</p>

<p>Most weeks there will also be some reading (or watching, or listening). Some weeks I’ll ask you to send me your thoughts on whatever it was (also due at 9pm the day before class) and we’ll talk a bit about the material in class. Some weeks you’ll present mini-presentations to the class based on those readings. These are part of your grade.</p>

<h2>Deadlines and Absences</h2>

<p>Deadlines matter a lot. In the life of a professional developer or designer, deadlines are constant and meeting them is what clients care about. If you miss a weekly submission deadline your assignment will be scored as zero.</p>

<p>If somehow something is going to make meeting a deadline or coming to class impossible, let me know well in advance. Any flexibility will be based on how professional you are with your approach.</p>

